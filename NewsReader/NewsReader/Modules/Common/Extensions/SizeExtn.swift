import Foundation

extension CGFloat {
    var size: CGSize { return CGSize(width: self, height: self)}
}

extension Int {
    var size: CGSize { return CGSize(width: self, height: self)}
}

extension Double {
    var size: CGSize { return CGSize(width: self, height: self)}
}


